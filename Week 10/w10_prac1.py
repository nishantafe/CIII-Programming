listOfThings = ["car", "boat", "caravan", "motor cycle", 42]

# Example of slicing a list
print(listOfThings[0:2])  # prints ['car', 'boat']

# Example of slicing a string
name = "birthday"
print(name[0:5])  # this prints "birth"
print(name[5:8])  # this prints "day"

# You can perform a calculation on a list item (int)
result = 5 + listOfThings[4]
print(result)

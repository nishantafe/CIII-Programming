name = "John"
age = 102
# Using the format() function
print("Hi this is {} and I am {}".format(name, age))

# Using the format() function
print("Hi this is {:10s} and I am {:10d}".format(name, age))

# Controlling the character spacing with the format() function
print("{:15s} {:10s} ".format("Name", "Age"))
print("{:15s} {}".format("John", 33))
print("{:15s} {}".format("Samuel", 103))
print("{:15s} {}".format("Samantha", 13))
print("{:15s} {}".format("Veronica", 27))
print("{:15s} {}".format("Christopher", 23))
print("{:15s} {}".format("Alessandro", 21))






